import java.util.*;

public class StaticBook {
    public String id;
    public String isbn;
    public String name;
    public String author;
    public String publish_date;

    public StaticBook(String id, String isbn, String name, String author, String publish_date){
        this.id = id;
        this.isbn = isbn;
        this.name= name;
        this.author = author;
        this.publish_date = publish_date;
    }

}
